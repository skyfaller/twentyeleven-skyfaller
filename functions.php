// add theme support for HTML5
function twentyeleven_add_html5_support() {
	add_theme_support(
		'html5',
		array(
			'search-form',
			'comment-form',
			'comment-list',
			'gallery',
			'caption',
			'script',
			'style',
		)
	);
}

add_action( 'after_setup_theme', 'twentyeleven_add_html5_support' );